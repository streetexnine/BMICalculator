    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            if (txtTinggi.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data tinggi,berat, bmi, dan ket harus lengkap! ");
            } else if (txtBerat.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data tinggi,berat, bmi, dan ket harus lengkap! ");
            } else if (txtistat.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data tinggi,berat, bmi, dan ket harus lengkap! ");
            } else if (txtisket.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data tinggi,berat, bmi, dan ket harus lengkap! ");
            } else if (txtBerat.getText().equals("") && txtBerat.getText().equals("") && txtistat.getText().equals("") && txtisket.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data tinggi,berat, bmi, dan ket harus lengkap! ");
            } else {
                sql = "INSERT INTO `dBMI` (`userID`, `Username`,`Tinggi`, `Berat`, `Bmi`, `Waktu`, `Keterangan`) VALUES ('" + lblID.getText() + "', '" + lblUser.getText() + "',"
                        + "'" + txtTinggi.getText() + "', '" + txtBerat.getText() + "', '" + txtistat.getText() + "', now(), '" + txtisket.getText() + "')";
                stat.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data Tersimpan! ");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_btnSaveActionPerformed

