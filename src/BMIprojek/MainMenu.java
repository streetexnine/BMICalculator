    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
        try {
            if (txtTinggi.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Data harus diisi lengkap");
            } else if (txtBerat.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Data harus diisi lengkap");
            } else if (txtTinggi.getText().equals("") && txtBerat.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Data harus diisi lengkap");
            } else {
                String keterangan = "";
                double tinggiB = Double.valueOf(txtTinggi.getText());
                tinggiB = tinggiB / 100;
                double beratB = Double.valueOf(txtBerat.getText());
                double bmi = beratB / (tinggiB * tinggiB);
                if (bmi > 27) {
                    keterangan = "Gemuk, Tingkat berat";
                } else if ((bmi >= 25.1) && (bmi <= 27)) {
                    keterangan = "Gemuk, Tingkat ringan";
                } else if ((bmi >= 18.5) && (bmi <= 25)) {
                    keterangan = "Ideal";
                } else if ((bmi >= 17) && (bmi <= 18.4)) {
                    keterangan = "Kurus, Tingkat ringan";
                } else {
                    keterangan = "Kurus, Tingkat berat";
                }
                txtistat.setText("" + bmi + "");
                txtisket.setText(keterangan);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnCalculateActionPerformed

